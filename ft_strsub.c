#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start,
size_t len)
{
	unsigned int	i;
	char			*str;

	i = 0;
	str = (char *)malloc(sizeof(*str) * (len + 1));
	if (!str || !s)
		return (NULL);
	while (i < len)
	{
		str[i] = s[start];
		i++;
		start++;
	}
	str[i] = '\0';
	return (str);
}
