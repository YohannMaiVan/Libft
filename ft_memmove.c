#include <string.h>

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	i;
	char	tmp;
	char	*s1;
	char	*s2;

	i = 0;
	s1 = (char *)dst;
	s2 = (char *)src;
	while (len)
	{
		tmp = s2[i];
		s1[i] = s2[i];
		i++;
		len--;
	}
	return (s1);
}
